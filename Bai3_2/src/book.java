
public class book {
	private String ma, ten, tg, loa, nxb;

	public book(String ma, String ten, String tg, String loa, String nxb) {
		super();
		this.ma = ma;
		this.ten = ten;
		this.tg = tg;
		this.loa = loa;
		this.nxb = nxb;
	}

	public String getMa() {
		return ma;
	}

	public void setMa(String ma) {
		this.ma = ma;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public String getTg() {
		return tg;
	}

	public void setTg(String tg) {
		this.tg = tg;
	}

	public String getLoa() {
		return loa;
	}

	public void setLoa(String loa) {
		this.loa = loa;
	}

	public String getNxb() {
		return nxb;
	}

	public void setNxb(String nxb) {
		this.nxb = nxb;
	}

}
